const path = require('path');
require('dotenv').config({ path: path.join(__dirname, '.env') });
const express = require('express');
const cors = require('cors');
const helmet = require('helmet');
const morgan = require('morgan');
const rateLimit = require('express-rate-limit');
const { connectDB } = require('./src/config/db');

const app = express();
const PORT = process.env.PORT || 3000;

// Trust proxy â€” å¿…è¦è¨­å®šï¼Œç³»çµ±é€éŽ nginx åå‘ä»£ç†éƒ¨ç½²
// è‹¥ä¸è¨­å®šï¼Œreq.ip æ°¸é æ˜¯ nginx çš„ IPï¼ŒRate Limiter å½¢åŒè™›è¨­
app.set('trust proxy', 1);

// ===== Rate Limiting =====
// å…¨åŸŸåŸºç¤Žé™æµï¼šåŒä¸€ IP æ¯åˆ†é˜æœ€å¤š 60 æ¬¡è«‹æ±‚
const globalLimiter = rateLimit({
  windowMs: 60 * 1000,
  max: 60,
  standardHeaders: true,
  legacyHeaders: false,
  message: { error: 'Too many requests, please try again later.' }
});

// AI ç”Ÿæˆåš´æ ¼é™æµï¼šåŒä¸€ IP æ¯åˆ†é˜æœ€å¤š 5 æ¬¡
const aiGenerationLimiter = rateLimit({
  windowMs: 60 * 1000,
  max: 5,
  standardHeaders: true,
  legacyHeaders: false,
  message: { error: 'AI generation rate limit exceeded. Please wait before generating again.' }
});

// ç ”ç©¶åˆ†æžé™æµï¼šåŒä¸€ IP æ¯åˆ†é˜æœ€å¤š 10 æ¬¡
const researchLimiter = rateLimit({
  windowMs: 60 * 1000,
  max: 10,
  standardHeaders: true,
  legacyHeaders: false,
  message: { error: 'Research API rate limit exceeded. Please try again later.' }
});

// Middleware
app.use(helmet());
app.use(cors({
  origin: process.env.CORS_ORIGIN ? process.env.CORS_ORIGIN.split(',') : '*'
}));
app.use(express.json());
app.use(morgan('dev'));
app.use(globalLimiter);

// Connect to Databases
connectDB();

// Routes
const authRoutes = require('./src/routes/authRoutes');
const projectRoutes = require('./src/routes/projectRoutes');
const keywordRoutes = require('./src/routes/keywordRoutes');
const researchRoutes = require('./src/routes/researchRoutes');
const articleRoutes = require('./src/routes/articleRoutes');

app.use('/api/auth', authRoutes);
app.use('/api/projects', projectRoutes);
app.use('/api/keywords', keywordRoutes);
app.use('/api/research', researchRoutes);
app.use('/api/articles', articleRoutes);

// Basic Routes
app.get('/', (req, res) => {
  res.json({ 
    service: 'ContentPilot Backend API',
    version: '0.1.0',
    status: 'running' 
  });
});

app.get('/health', (req, res) => {
  res.json({ 
    status: 'ok', 
    timestamp: new Date().toISOString(),
    env: process.env.NODE_ENV
  });
});

// Backward-compatible API health endpoint (used by external scripts/monitoring)
app.get('/api/health', (req, res) => {
  res.json({
    status: 'ok',
    timestamp: new Date().toISOString(),
    env: process.env.NODE_ENV
  });
});

// Error Handling
app.use((err, req, res, next) => {
  console.error(err.stack);
  res.status(500).json({ error: 'Something went wrong!' });
});

// Start Server
app.listen(PORT, () => {
  console.log(`ðŸš€ Server is running on port ${PORT}`);
  console.log(`   Environment: ${process.env.NODE_ENV}`);
});
